class base:
	"""
	base is basic math library
	"""
	def add(a, b):
		"""
		add two integers and return the result
		"""
		return a + b

	def subtract(a,b):
		"""
		subract two integers and return the result
		"""
		return a - b

	def multiply(a,b):
		"""
		multiply two integers and return the result
		"""
		return a * b

	def divide(a,b):
		"""
		divide two integers and return the result
		"""
		return a / b

	def get_remainder(a,b):
		"""
		get remainder after dividing two integers and return the result %
		"""
		return a % b